document.getElementById('applyFilter').addEventListener('click',()=>{
    const element=document.querySelector('input[name="filterOption"]:checked')
    const answer=element.value;

    if(answer==="rating"){
          resturants.sort((a,b)=>b.Rating-a.Rating);// decending order  
    }
    else if(answer==="highLow"){
          resturants.sort((a,b)=>b.Price_for_two-a.Price_for_two);// decending order  
    }
    else if(answer==='costLowHigh'){
         resturants.sort((a,b)=>a.Price_for_two-b.Price_for_two)
    }
    else if(answer==="distance"){
         resturants.sort((a,b)=>a.Distance_from_Customer_house-b.Distance_from_Customer_house)
    }

    document.getElementById("root").replaceChildren();
        document.getElementById('filterPopup').classList.add("hidden");

    getResturants(resturants)
})

